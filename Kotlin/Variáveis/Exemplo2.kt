 * Apesar de não ser preciso declarar o tipo da variável, pois Kotlin faz isso
 * implicitamente, é possível especificar o tipo delas.
 * Besides it is not needed to declare the variable type, since Kotlin does it
 * implicitly, it is possible to specify its type.
